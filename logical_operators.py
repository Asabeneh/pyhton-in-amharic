print(2 > 1 and 8 > 7)  # True and True => True
print(8 > 2 ** 3 and 7 > 6)  # False and True = > False
print(4 < 3 and 10 < 9)  # False and False => False

print(2 > 1 or 8 > 7)  # True or True => True
print(8 > 2 ** 3 or 7 > 6)  # False or True = > True
print(4 < 3 or 10 < 9)  # False or False => False
print(not 8 > 6)
